/* 
* Aritméticos
* + = adição 
* ** = potenciação 
* % = resto da divisão
* * = multiplição
* / = divisão
*/ 
const num1 = 10;
const num2 = 5;
const num3 = 2;
console.log(num1 + num2);
console.log(num1 ** num2);
console.log(num1 / num3);
console.log(num1 + num2 * num3);
console.log((num1 * num2) / num3);